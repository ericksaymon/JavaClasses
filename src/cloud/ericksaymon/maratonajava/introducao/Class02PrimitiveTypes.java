package cloud.ericksaymon.maratonajava.introducao;

public class Class02PrimitiveTypes {
    public static void main(String[] args) {
        // Vão guardar um valor simples em memória.
        // Os tipos são int, double, float
        // char, byte, short, long e boolean
        int age = 12;
        long bigNumber = 10000L;
        double salaryDouble = 10000.00;
        float salaryFloat = 15000.00f;
        byte numberByte = 127;
        short numberShort = 32000;
        boolean bool = true;
        char character = 'A';

        String thisIsAString = "Hello World";
        System.out.println(thisIsAString);
        System.out.println("The age is: " + age);
    }
}
